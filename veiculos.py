class Veiculo:
    def __init__(self, fabricante, modelo, pais, posicaoX, posicaoY, valor, passageiros):
        self.fabricante = fabricante
        self.modelo = modelo
        self.pais = pais
        self.posicaoX = posicaoX
        self.posicaoY = posicaoY
        self.valor = valor
        self.passa = passageiros


def info():
    print("==> Dados do veiculo")
    print("Fabricante: " + self.fabricante)
    print("Modelo: " + self.modelo)
    print("Pais: " + self.pais)
    print("PosicaoX: " + self.posicaoX)
    print("PosicaoY: " + self.posicaoY)
    print("Valor: " + self.valor)
    print("Passageiros: " + self.passageiros)
    print("---\n" * 30)


